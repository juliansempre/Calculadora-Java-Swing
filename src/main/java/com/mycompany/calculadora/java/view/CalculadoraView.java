    public void resultado() {
    String expressao = txtTela.getText();
    
    // Verifica se a expressão contém um operador (+, -, *, /)
    if (expressao.contains("+") || expressao.contains("-") || expressao.contains("*") || expressao.contains("/")) {
        // Divide a expressão em operandos e operadores
        String[] partes = expressao.split("\\s*(?=[+\\-*/])|(?<=[+\\-*/])\\s*");
        
        // Verifica se a expressão foi dividida corretamente
        if (partes.length == 3) {
            try {
                double operando1 = Double.parseDouble(partes[0]);
                String operador = partes[1];
                double operando2 = Double.parseDouble(partes[2]);
                double resultado = 0.0;

                // Realiza o cálculo com base no operador
                switch (operador) {
                    case "+":
                        resultado = operando1 + operando2;
                        break;
                    case "-":
                        resultado = operando1 - operando2;
                        break;
                    case "*":
                        resultado = operando1 * operando2;
                        break;
                    case "/":
                        resultado = operando1 / operando2;
                        break;
                }

                txtTela.setText(Double.toString(resultado));
            } catch (NumberFormatException e) {
                // Tratamento de erro se os operandos não forem números válidos
                txtTela.setText("Erro de cálculo");
            }
        } else {
            // Tratamento de erro se a expressão não estiver correta
            txtTela.setText("Expressão inválida");
        }
    } else {
        // Tratamento de erro se a expressão não contiver um operador
        txtTela.setText("Expressão inválida");
    }
}

