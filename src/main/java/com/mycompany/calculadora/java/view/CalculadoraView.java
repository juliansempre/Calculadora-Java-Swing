    public void digitar(String numero){
            String gtexto =  txtTela.getText();
        StringBuilder sbg = new StringBuilder(gtexto);
        StringBuilder getTextInvertido = sbg.reverse();
        
        String texto = numero + getTextInvertido ;
        StringBuilder sb = new StringBuilder(texto);
        sb.reverse();
        String textoInvertido = sb.toString();
        txtTela.setText(textoInvertido);
    }

